/*
* returns video_mode_table for a perticular
* index depending on cea and hdmi vic
*/
const struct video_mode_type *sii_vid_timing_from_vic(uint8_t cea_vic,
		uint8_t hdmi_vic)
{
	if ((cea_vic || hdmi_vic) &&
		((cea_vic <= 107) && (hdmi_vic <= 4))) {
		struct hdmi_to_cea_vic h2c_vic = (hdmi_vic <= 4) ?
			hdmi_to_cea_table[hdmi_vic] : hdmi_to_cea_table[0];
		uint8_t index = cea_vic ? vic_to_idx_table[cea_vic] :
			vic_to_idx_table[h2c_vic.cea_vic1];
		return &video_mode_table[index];
	} else {
		return NULL;
	}
}

uint8_t sii_map_hdmi_vic_to_cea_vic(uint8_t hdmi_vic)
{
	struct hdmi_to_cea_vic h2c_vic = (hdmi_vic < 5) ?
		hdmi_to_cea_table[hdmi_vic] : hdmi_to_cea_table[0];
	return h2c_vic.cea_vic1;
}
