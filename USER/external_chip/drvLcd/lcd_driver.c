void display_string_8x16(uint page,uint column,unsigned char u8InverColor,unsigned char *text)
{
    uint i=0,j,k,n;
    while(text[i]>0x00)
    {
        if((text[i]>=0x20)&&(text[i]<=0x7e))
        {
            j=text[i]-0x20;
            for(n=0;n<2;n++)
            {
                lcd_address(page+n,column);
                for(k=0;k<8;k++)
                {

                    if(u8InverColor)
                        transfer_data(ascii_table_8x16[j][k+8*n]^0xFF);//显示 5x7 的 ASCII 字到 LCD 上，y 为页地址，x 为列地址，最后为数据
                    else
                        transfer_data(ascii_table_8x16[j][k+8*n]);//显示 5x7 的 ASCII 字到 LCD 上，y 为页地址，x 为列地址，最后为数据
                }
            }
            i++;
            column+=8;
        }
        else
        i++;
    }
}

