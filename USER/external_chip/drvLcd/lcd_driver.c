//写指令到 LCD 模块
void transfer_command(unsigned char data)
{
	 char i;
	 led_cs(0);
	 lcd_delay(2);
	 led_rs(0);
	 lcd_delay(2);
	 for(i=0;i<8;i++)
	 {
		 led_sclk(0);
		 lcd_delay(10);
		 if(data&0x80) led_data(1);
		 else led_data(0);
         lcd_delay(25);	//50
		 led_sclk(1);
		 lcd_delay(10); //20
		 data=data<<=1;
	 }
     lcd_delay(2);
	 led_cs(1);
	 lcd_delay(2);
}

