//LCD 模块初始化
void initial_lcd(void)
{
    if(pSeriesLcdInitFunc != NULL)
    {
        pSeriesLcdInitFunc();
    }
    else
    {
        led_reset(0); //低电平复位
        delay(100);
        led_reset(1); //复位完毕
        delay(80);
        transfer_command(0xe2); //软复位
        delay(5);
        transfer_command(0x2f); //打开内部升压
        delay(5);

        led_backlight(1);

        transfer_command(0x81); //微调对比度
        transfer_command(0x38); //微调对比度的值，可设置范围 0x00～0xFF(原值 0X38）
        transfer_command(0xeb); //1/9 偏压比（bias）
        transfer_command(0xc6); //行列扫描顺序：从上到下、从左到右
        transfer_command(0xaf); //开显示
        transfer_command(0xc2); //横向显示

        clear_screen(); //clear all dots
    }
}

